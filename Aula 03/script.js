// Strings
console.log('Hello World!'); 
console.log("Hello World!");
console.log(`Hello World!`);
console.log(`"Hello" 'World!'`);

// Int
console.log(35);

// Float
console.log(1.25);

// Tipos de dados em JS
console.log(typeof(1.24)); // Number
console.log(typeof(112)); // Number

console.log(typeof('Hello World!')); // String
console.log(typeof(`Hello World!`)); // String